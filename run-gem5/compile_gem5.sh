#!/bin/bash

# -----------------------------------------------------------
# 1、配置环境 
# -----------------------------------------------------------

# 核心数
export PARALLEL_NUM=16

# 当前工作目录
export WORKDIR=$(pwd)

# 其他环境
export gem5_home=${WORKDIR}/GEM5

# -----------------------------------------------------------
# 2、执行过程 
# -----------------------------------------------------------

# 构建内存模拟器
cd ${gem5_home}
bash ./init.sh

# 构建gem5 
scons build/RISCV/gem5.opt --gold-linker -j${PARALLEL_NUM}
